// you cannot use a space or hyphen in a variable name
// example:-
// first name (invalid)
// first-name (invalid)
// firstName (valid)

// var first name = "John"; // invalid
// var first-name = "John"; // invalid
var firstName = "John"; // valid it is also called as camel case writing first letter of the second word is capital

console.log(firstName);


